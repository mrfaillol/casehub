def create_forms():
    """Create all USCIS form templates in the database."""
    db = SessionLocal()

    try:
        # Get admin user for created_by
        admin = db.query(User).filter(User.email == "admin@immigrant.law").first()
        admin_id = admin.id if admin else 1

        # Delete existing test questionnaire if exists
        from models.questionnaire import QuestionnaireFieldResponse, QuestionnaireResponse

        print("Clearing existing questionnaire data...")
        db.query(QuestionnaireFieldResponse).delete()
        db.query(QuestionnaireResponse).delete()
        db.query(QuestionnaireField).delete()
        db.query(QuestionnaireTemplate).delete()
        db.commit()

        print(f"\nCreating {len(USCIS_FORMS)} USCIS form templates...\n")

        for form_data in USCIS_FORMS:
            # Create template
            template = QuestionnaireTemplate(
                name=form_data["name"],
                description=form_data["description"],
                category=form_data["category"],
                target_type="client",
                is_active=True,
                is_required=False,
                created_by=admin_id
            )
            db.add(template)
            db.flush()  # Get the template ID

            # Create fields
            for order, field_data in enumerate(form_data["fields"]):
                field = QuestionnaireField(
                    template_id=template.id,
                    field_name=field_data["field_name"],
                    label=field_data["label"],
                    field_type=field_data["field_type"],
                    is_required=field_data.get("is_required", False),
                    section=field_data.get("section"),
                    options=field_data.get("options"),
                    order=order
                )
                db.add(field)

            print(f"  ✓ {form_data['form_number']}: {len(form_data['fields'])} fields")

        db.commit()
        print(f"\n✅ Successfully created {len(USCIS_FORMS)} USCIS form templates!")

    except Exception as e:
        db.rollback()
        print(f"❌ Error: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    create_forms()
