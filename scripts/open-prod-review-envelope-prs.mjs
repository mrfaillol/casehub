#!/usr/bin/env node

import childProcess from "child_process";
import fs from "fs";
import path from "path";

const publishedIssuesPath = path.resolve(process.env.CASEHUB_PROD_REVIEW_PUBLISHED_ISSUES || process.argv[2] || "");
const repository = process.env.GITHUB_REPOSITORY || "";
const dryRun = process.env.CASEHUB_PROD_REVIEW_DRY_RUN === "1";
const runId = process.env.GITHUB_RUN_ID || new Date().toISOString().replace(/[:.]/g, "-");
const pendingOpsRoot = path.resolve(
  process.env.CASEHUB_PROD_REVIEW_PENDING_OPS_DIR || path.join("pending-ops", "prod-review-factory", runId),
);
const requiredLabels = ["army-bot", "needs-agent-pickup", "prod-review-factory"];
const labelDefinitions = {
  "army-bot": ["6f42c1", "PR/envelope generated by automation"],
  "needs-agent-pickup": ["d93f0b", "Task envelope is waiting for an agent pickup"],
  "prod-review-factory": ["1d76db", "Generated or updated by the production review factory"],
};

function fail(message) {
  console.error(message);
  process.exit(1);
}

function run(command, args, options = {}) {
  return childProcess.execFileSync(command, args, {
    encoding: "utf8",
    stdio: options.input ? ["pipe", "pipe", "pipe"] : ["ignore", "pipe", "pipe"],
    input: options.input,
    env: process.env,
  });
}

function tryRun(command, args) {
  const result = childProcess.spawnSync(command, args, { encoding: "utf8", env: process.env });
  return { ok: result.status === 0, stdout: result.stdout || "", stderr: result.stderr || "" };
}

function errorText(error) {
  return String(error?.stderr || error?.stdout || error?.message || error || "").trim();
}

function isAuthOrPushFailure(error) {
  const text = errorText(error).toLowerCase();
  return /401|403|bad credentials|permission denied|not authorized|resource not accessible by integration|write access to repository not granted|could not read username|authentication failed|repository not found|protected branch/i.test(text);
}

function ensureLabel(name) {
  if (dryRun) return;
  const [color, description] = labelDefinitions[name] || ["ededed", `CaseHub label: ${name}`];
  childProcess.spawnSync(
    "gh",
    [
      "api",
      "--method",
      "POST",
      `repos/${repository}/labels`,
      "-f",
      `name=${name}`,
      "-f",
      `color=${color}`,
      "-f",
      `description=${description}`,
    ],
    { encoding: "utf8", stdio: "ignore", env: process.env },
  );
}

function labelIssue(number) {
  if (dryRun || !number) return;
  run("gh", ["api", "--method", "POST", `repos/${repository}/issues/${number}/labels`, "--input", "-"], {
    input: JSON.stringify({ labels: requiredLabels }),
  });
}

function existingPrForBranch(branch) {
  const output = run("gh", ["pr", "list", "--repo", repository, "--head", branch, "--state", "open", "--json", "number,title,url", "--limit", "1"]);
  const prs = JSON.parse(output || "[]");
  return prs[0] || null;
}

function remoteBranchExists(branch) {
  const result = tryRun("git", ["ls-remote", "--heads", "origin", branch]);
  return result.ok && result.stdout.trim().length > 0;
}

function yamlString(value) {
  return JSON.stringify(String(value ?? ""));
}

function yamlList(values, indent = 2) {
  const prefix = " ".repeat(indent);
  return values.map((value) => `${prefix}- ${yamlString(value)}`).join("\n");
}

function envelopeYaml(issue) {
  const labels = Array.isArray(issue.labels) ? issue.labels : [];
  const acceptance = [
    "Reproduce the finding from the linked production review issue.",
    "Patch the affected route, service, template, static asset, script, or documentation path within the allowed scope.",
    "Attach validation evidence and reference the production review artifact or a fresh audit run.",
  ];
  return [
    'schema_version: "0.1"',
    "source: prod-review-factory",
    `source_url: ${yamlString(issue.url)}`,
    `issue_number: ${Number(issue.number)}`,
    `issue_title: ${yamlString(issue.title)}`,
    "requested_runtime: codex-wing",
    "ledger_commit_sha: TO_BE_FILLED_BY_RUNNER",
    "council_required: false",
    "budget:",
    "  max_tokens: 100000",
    "  max_turns: 10",
    "  timeout_seconds: 1800",
    "paths_allowed:",
    yamlList(["routes", "services", "templates", "static", "scripts", "docs"], 2),
    "paths_forbidden:",
    yamlList([".env*", "credentials/", "*.pem", "*.key", "**/secrets/"], 2),
    "secrets_allowed: false",
    "human_review_required: true",
    "hmac_signature: PENDING_SIGN",
    "payload:",
    "  kind: bugfix",
    "  source: prod-review-factory",
    `  issue_number: ${Number(issue.number)}`,
    `  issue_url: ${yamlString(issue.url)}`,
    "  labels:",
    yamlList(labels, 4),
    `  fingerprint: ${yamlString(issue.fingerprint || "")}`,
    "  acceptance_criteria:",
    yamlList(acceptance, 4),
    "",
  ].join("\n");
}

function writePendingOperation(issue, branch, phase, error) {
  fs.mkdirSync(pendingOpsRoot, { recursive: true });
  const issueNumber = Number(issue.number);
  const safeIssue = Number.isFinite(issueNumber) ? String(issueNumber) : "unknown";
  const payload = {
    repository,
    created_at: new Date().toISOString(),
    run_id: runId,
    phase,
    branch,
    reason: "remote_mutation_failed",
    error: errorText(error).slice(0, 4000),
    issue: {
      number: issueNumber || null,
      title: issue.title || "",
      url: issue.url || "",
      fingerprint: issue.fingerprint || "",
      labels: Array.isArray(issue.labels) ? issue.labels : [],
    },
    envelope_yaml: envelopeYaml(issue),
  };
  const outPath = path.join(pendingOpsRoot, `issue-${safeIssue}.json`);
  fs.writeFileSync(outPath, `${JSON.stringify(payload, null, 2)}\n`);
  return { path: outPath, payload };
}

function createBranchCommitAndPush(issue, branch) {
  if (dryRun) return;
  run("git", ["checkout", "-B", branch, "origin/main"]);
  const envelopePath = path.join("tasks", "queue", "_drafts", `prod-review-issue-${issue.number}.yaml`);
  fs.mkdirSync(path.dirname(envelopePath), { recursive: true });
  fs.writeFileSync(envelopePath, envelopeYaml(issue));
  run("git", ["add", envelopePath]);
  run("git", ["commit", "-m", `chore(factory): prod review envelope for issue #${issue.number}`]);
  run("git", ["push", "-u", "origin", branch]);
}

function openPr(issue, branch) {
  const body = [
    "Production review factory handoff.",
    "",
    `Source issue: ${issue.url || `#${issue.number}`}`,
    "",
    "This PR adds a signed task-envelope draft candidate for agent pickup. The envelope keeps production secrets out of scope and requires human review before execution is promoted.",
  ].join("\n");
  if (dryRun) {
    return { number: null, title: `[prod-review-factory] Envelope for issue #${issue.number}`, url: null };
  }
  const response = run("gh", ["api", "--method", "POST", `repos/${repository}/pulls`, "--input", "-"], {
    input: JSON.stringify({
      base: "main",
      head: branch,
      title: `[prod-review-factory] Envelope for issue #${issue.number}`,
      body,
    }),
  });
  return JSON.parse(response);
}

if (!publishedIssuesPath || publishedIssuesPath === path.resolve("")) {
  fail("Usage: open-prod-review-envelope-prs.mjs <published-issues.json> or set CASEHUB_PROD_REVIEW_PUBLISHED_ISSUES");
}
if (!repository) {
  fail("GITHUB_REPOSITORY is required");
}
if (!fs.existsSync(publishedIssuesPath)) {
  fail(`Published issues JSON not found: ${publishedIssuesPath}`);
}

for (const label of requiredLabels) ensureLabel(label);

const published = JSON.parse(fs.readFileSync(publishedIssuesPath, "utf8"));
const issues = Array.isArray(published.issues) ? published.issues.filter((issue) => issue.number) : [];
const opened = [];
const pendingOps = [];

if (!dryRun) {
  run("git", ["config", "user.name", "prod-review-factory[bot]"]);
  run("git", ["config", "user.email", "prod-review-factory@users.noreply.github.com"]);
  run("git", ["fetch", "origin", "main", "--depth", "1"]);
  if (!tryRun("git", ["rev-parse", "--verify", "origin/main"]).ok) {
    run("git", ["update-ref", "refs/remotes/origin/main", "FETCH_HEAD"]);
  }
}

for (const issue of issues) {
  const branch = `bot/prod-review-envelope-issue-${issue.number}`;
  try {
    const existing = existingPrForBranch(branch);
    if (existing) {
      labelIssue(existing.number);
      opened.push({ status: "exists", issue_number: issue.number, branch, pr: existing });
      continue;
    }

    const branchExists = remoteBranchExists(branch);
    if (!branchExists) {
      createBranchCommitAndPush(issue, branch);
    }
    const pr = openPr(issue, branch);
    labelIssue(pr.number);
    opened.push({
      status: dryRun ? "dry-run-open" : branchExists ? "opened-from-existing-branch" : "opened",
      issue_number: issue.number,
      branch,
      pr: { number: pr.number, title: pr.title, url: pr.html_url || pr.url || null },
    });
  } catch (error) {
    if (!isAuthOrPushFailure(error)) {
      throw error;
    }
    const pending = writePendingOperation(issue, branch, "open-envelope-pr", error);
    const pendingPath = path.relative(process.cwd(), pending.path);
    pendingOps.push({ issue_number: issue.number, branch, path: pendingPath });
    opened.push({ status: "pending-op", issue_number: issue.number, branch, pending_op: pendingPath });
    console.error(`Remote mutation failed; wrote pending operation to ${pending.path}`);
    continue;
  }
}

const outputPath = path.join(path.dirname(publishedIssuesPath), "opened-envelope-prs.json");
fs.writeFileSync(outputPath, `${JSON.stringify({ repository, dryRun, publishedIssuesPath, pull_requests: opened, pending_ops: pendingOps }, null, 2)}\n`);
console.log(outputPath);
if (pendingOps.length) {
  process.exitCode = 78;
}
